// ============================================================================
// agent/utils —— Agent 专属工具函数
// ============================================================================
//
// 职责边界：
// 存放 agent 内部专用的纯工具函数（无副作用、无 I/O）。与 shared/text.rs
// 的区别：shared 放跨层通用纯函数，utils 只放 agent 专用、不便下沉到
// shared 的函数。
//
// 应包含的文件：
// - length_normalizer.rs  长度归一化（按 token/字符统一计量，喂给 budget）
// - 其他 agent 专用纯函数（按需新增，保持单一职责）
//
// 依赖规则：
// - 仅依赖 shared、std
// - 严禁依赖任何业务域、infra、Tauri
// - 不依赖 agent/ 内其他子模块（被依赖方，保持纯工具层）
//
// 被依赖方：
// - agent/ 内所有子模块（按需调用）
//
// 注意：
// - 若某函数被 infra 或业务域需要，应下沉到 shared/，不留在本模块
// - 不放有副作用的函数（写文件/发请求等），那些属于 infra 或 tools/
